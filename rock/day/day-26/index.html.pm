只能说油管自动推荐经常冲刷出宝石来啊，之前福居良也是油管推荐的。“Meditaçao” 是 A.C. Jobim 写的，喜欢星际牛仔的人一定认识他：那三个老头就叫 Antônio，Carlos 和 Jobim，估计是◊ruby{菅◊rt{jiān}}野洋子致敬 A.C. Jobim。

那个光头老哥就是 Joan Chamorro，他好像办了个音乐学校，培养很多爵士音乐人，很厉害。可以看看他的◊link["https://www.youtube.com/user/sergech"]{油管频道}，和他乐团的 Band Camp 主页：◊link["https://santandreujazzband.bandcamp.com"]{Sant Andreu Jazz Band}。他们的 ◊em{Jazzing} 系列挺不错的。

另外推荐一张 Andrea Motis 的现场专辑 ◊link["https://andreamotisjoanchamorro.bandcamp.com/album/live-at-jamboree-barcelona"]{◊em{Live at Jamboree, Barcelona}}。很不错，油管上还有视频：◊link["https://youtu.be/bo8og3xw-Ys"]{I Fall In Love}。

