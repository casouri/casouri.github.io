我竟然没发过这首歌。这首是之前在推上还是油管看到的。非常、非常振奋人心的歌。看着画面中的女孩流着泪奔跑，听着背景的合唱，心中的淤塞被一扫而空，心情像鸟一样展翅高飞。音乐和美术竟然有如此难以置信的力量，太感动了！心情不好的时候请一定听一听（配合动画）！

