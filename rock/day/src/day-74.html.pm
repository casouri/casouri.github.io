之前听说 Spotify 上有很多很棒的日本独立音乐，就去充了个会员，现在看来所言不虚。我最近已经完全沉溺于 JPOP 的温柔乡中www 这张就是典型的一张魅惑我的靡靡之音：空灵清脆的声音，优美新颖的旋律，夫复何求？

这张专辑每首都很好听，比较有趣的是「GIRL」里唱的“BFF”，是日本人那种漏风的介于“fu”和“hu”之间的发音，感觉很可爱www

