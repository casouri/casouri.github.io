我一般不发这种靠着 riff 好听取胜的单曲。但是这首不太一样，我很喜欢 riff 里口琴的声音（听起来像十二孔口琴？）––给人一种非常潇洒闲适、夏夜浴衣的感觉。结合歌词让人脑海里浮现出有点潮的少男少女在东京街头闲逛消磨时间的景象。我想了一下，我年少时好像没有这种经历，以后也大概不会有，亏了！

不过我突然想起来一次一个人去吃完烤肉，在凉爽的北京夏夜骑车回家的情景，当时愉快的心情渐渐和这首歌的情绪重合了。谢谢这首歌，和写博客的我，让我想起来了美好的回忆www

