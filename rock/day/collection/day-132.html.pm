这张专辑几个月前被油管算法发掘出来小小流行了一下。当时听了好几遍，挺喜欢的。但是听了好几遍以后感觉淡了一点，犹豫了一下没发。最近又出现在我首页上，听了一遍又喜欢上了。非常可爱清凉的声音。这张专辑也被归类为涩谷系，之前的◊link["../day-129.html"]{Sayonara Ponytail}也是。

我最喜欢的当然是第一首 ◊em{Best Friend}，太可爱了。另外，◊jpns{「空へ」}的开头听着超像 ◊em{Sweet Home Alabama} 啊。

这个乐队跟其他在油管时代意外流行起来的九十年代 JPop 乐队一样，在当时没泛起什么水花。不过◊link["https://www.reddit.com/r/japanesemusic/comments/1diwlrj/seeking_information_regarding_satellite_lovers/"]{在 Reddit 上有个帖子}，里面网友搜到好多信息。给乐队拍封面的摄影师后来跟封面上的主唱结婚了，摄影师还有个instagram，上面还能看到他们现在的照片www