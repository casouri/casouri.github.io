写上一篇的时候想到了这首歌。感觉放在前面那一篇下面有点可惜，所以给它单独一篇。一开始听到这首歌是在哔哩哔哩，看到链接的视频。◊jpns{椎名林檎}也是这样认识的。视频真的超酷，晃晃悠悠的镜头，火热的气氛，一脸不爽的表情，露出脖颈的和服，弹舌，小碎步，跺脚，超棒www

这张专辑里其他的歌我也很喜欢，比如◊jpns{「幸福論（悦楽編）」}，扩音器超帅。很难相信这张专辑是1999年出版的！换到今年发售也完全不过时。

