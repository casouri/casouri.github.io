我一直觉得这首歌有浓重的中国风格，滚石也提到这首歌受东亚风格影响，不过他 Mick Jagger 觉得是印度音乐影响。不管他怎么想，对我来说这首歌字里行间全是中国曲调。尤其是“just about a moonlight mile”那段。音乐里听来的这种感觉模模糊糊，描述不出来，只能大概感觉到元宵节万家灯火的那种和美的温馨。并不喧闹，是从远处高空看到星星点点的火光的安静的温馨。

