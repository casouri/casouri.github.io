这张专辑怎么遇到的呢？我上个月突发奇想搜了一下“中国地下摇滚”，其中看到了琪琪音像，这么可爱的名字当然要搜一下。然后发现他们有 Bandcamp。Bandcamp 上看到了这张专辑（封面挺好看）。编曲感觉并不复杂，但是就是很好听，能感到欢乐，充满能量www

琪琪音像的标志我挺喜欢，一开始以为是捂着脸的少女，仔细看才发现是手拖着脸，有三只眼睛的少女。没关系，以后我要是开公司，商标就是捂着脸的简笔画短发少女。

琪琪音像还有不少别的专辑，大部份是这种迷幻风的，有几个挺不错的，比如 ◊link["https://qiiisnacksrecords.bandcamp.com/album/idle-archipelago"]{◊em{Idle Archipelago}}，◊link["https://qiiisnacksrecords.bandcamp.com/album/fondle-reissue"]{◊em{Fondle}}（我喜欢这张的封面），还有◊link["https://imdifficult.bandcamp.com/track/kids-live-staged"]{《小​孩》}（我很喜欢这张的封面），但是这张给我的印象比较深，可能因为更大众化吧。