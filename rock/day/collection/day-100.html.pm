Spotify 上听到的（可能是《银河护卫队》的歌单？），讲的是芝加哥的一条公路，Lake Shore Drive。我看维基百科之前想象的是山里的湖边公路，开着敞篷车，湖里波光粼粼，风吹过头发。背景的钢琴给人一种并不急迫的、轻盈的速度感，有些像翻转的 ◊link["https://youtu.be/5XP5RP6OEJI"]{◊em{Der Erlkönig}}。

专辑里的其他歌没有这么精彩的，就不提了。

