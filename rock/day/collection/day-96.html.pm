之前朋友介绍了 Elvis 抒情的歌，比如 ◊em{Can’t Help Falling in Love With You}，我才知道他除了摇滚歌以外还可以这么酥。最近 Spotify 上听了听他的歌单，这首 ◊em{If I Can Dream} 抓住了我，里面蕴含的情感浓郁欲滴。看着歌词又听一遍，把我感动到眼眶湿润。之后查了这首歌当时的背景，感觉更传奇了。

今年还有部 Elvis 的电影，在 Spotify 上看到了原声辑，里面有很多当代歌手改编的 Elvis 的名曲，这种传承致敬也很感动。有时间一定去把电影看了！

Bonus: ◊link["https://beta.prx.org/stories/162320"]{◊em{Elvis Presley: If I Can Dream}}。他们把 Elvis 的歌重新制作，加上 Royal Philharmonic Orchestra 的 backing，发型了张专辑。这个广播节目讲了一些幕后故事，还放了专辑里的歌，每首都很美！

