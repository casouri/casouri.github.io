日文标题是◊jpns{オリジナル・サウンドトラック「機動戦士ガンダム サンダーボルト」}，为了读者的心理健康，我用英文的标题了。

之前就看过一个◊link["https://youtu.be/eMSQ2xqjX7Q"]{评价雷霆宙域音乐的油管视频}，朋友还推荐过，这两天终于和朋友一起看了，爵士机战果然名不虚传！忽隐忽现的爵士广播和高达杀神，太帅了。联邦吉翁主角对战，流行 v 爵士太帅了。而最终决战的蓝调 v 爵士更是苍凉潇洒与热血狂乱并行。

动画本身也很不错，战争的疯狂、血腥、绝望很好地表现了出来。

OST当然非常棒，开头的 bebop/free jazz 十分刺激，咆哮的低音单簧管让人血液加速，后半段的 R&B，爵士，民谣都很好听，最后还来了一首……电子金属？

