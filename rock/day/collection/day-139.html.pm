我以为我写了这张，结果并没有，只是在手机的笔记里随便写了几句。年底这几个月没什么精神啥都不想干，积了好多没写。

这张专辑是开启我年底美国这边独立音乐循环的一首。好听，震撼，昏睡神曲。充满了每一寸空间的、低沉的震颤的声音浸泡你的大脑，振动、麻痹你的神经，让你不知不觉间陷入昏迷。不是孱弱的把你无聊到睡觉的催眠，而是 gigachad 的通过强大颤震动让你物理昏迷的催眠。旋律好听，歌词也莫名其妙神魔鬼样，喜欢。

每首都好听，可以从前三首开始：◊em{The Rolling Stones}，◊em{Nausicaä}，◊em{Love Takes Miles}。我看到 Nausicaä 的时候还奇怪，怎么跟风之谷扯上关系了？结果发现 Nausicaä 是奥德赛里的人物，孤陋寡闻了，惭愧。

这老哥挺神的，除了这张专辑，他的乐队 Geese 的另两张专辑 ◊em{3D Country} 和 ◊em{Getting Killed} 也很好听，就是比这张个人专辑更闹腾一点。

要说这张专辑的缺点，封面有点丑。这个呆滞的死亡大头照加两条链子是啥啊（？）