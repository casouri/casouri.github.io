上星期开车去 Arizona 看弟弟，顺便去大峡谷逛了一圈。几天下来开车开了个够。我本来是很怕开长途的，因为开不了一个小时就会开始困得不行。但最近发现了灵丹妙药：开车的时候听播客就一点都不困。唯一的问题是播客听一集少一集，开车回家的末尾听完了出门前准备的存货。最后的一个多小时是听 The Band 的 ◊em{Greatest Hits} 开完的，其中一首 ◊em{Tears of Rage} 我非常喜欢，之前竟然被我错过了。我尤其喜欢◊fnref["horn"]{法国号}加入的时候，还有几个人合唱但又微微错开的时候。Bob Dylan 的歌词也有感情。

这张封面……是 Bob Dylan 画的。乍一看好丑，但是越看越感觉……还行？

我本来只是想介绍 ◊em{Tears of Rage} 一首，但其实整张专辑都美妙非凡，◊em{The Weight}，◊em{Lonesome Suzie}，◊em{I Shall Be Released} 尤其值得一听。另外推荐读一读这两篇评论：◊link["https://jmeshel.com/126-bob-dylan-tears-of-rage-the-basement-tapes/"]{◊em{126: Bob Dylan, ‘Tears of Rage’ (The Basement Tapes)}} 和 ◊link["https://jmeshel.com/127-the-band-tears-of-rage-music-from-big-pink/"]{◊em{127: The Band, ‘Tears of Rage’ (“Music from Big Pink”)}}。

