也是 Spotify 上听到的。这首歌的背景故事挺有趣：

◊bquote{
“Rich Girl” was written about an old boyfriend of Sara’s [Allen] from college that she was still friends with at the time. His name is Victor Walker. He came to our apartment, and he was acting sort of strange. His father was quite rich. I think he was involved with some kind of a fast-food chain. I said, “This guy is out of his mind, but he doesn’t have to worry about it because his father’s gonna bail him out of any problems he gets in.” So I sat down and wrote that chorus. [Sings] “He can rely on the old man’s money/he can rely on the old man’s money/he’s a rich guy.” I thought that didn’t sound right, so I changed it to “Rich Girl.” He knows the song was written about him. (◊link["https://americansongwriter.com/hall-and-oates-soul-survivors/"]{◊em{HALL AND OATES: Soul Survivors}}, Ken Sharp, 2009)
}

