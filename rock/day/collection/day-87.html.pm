我一般不听金属，尤其他们还喜欢特阴间的艺术风格。但这张还挺有意思的。前几天一直在风花雪月，今天来个阴间猛男调和一下也不错。这个乐队是我在哔哩哔哩一个中国摇滚乐队介绍视频看到的。那个视频里全是文青软绵绵后摇，一个个有气无力，就这一个乐队很有精神。这张专辑里民族乐器的用法我很喜欢，尤其是第一首《◊trad{湘靈鼓瑟}》里的打击乐器。突兀的暂停用的也很妙。

这张专辑很容易在 bandcamp 上买到：◊link["https://jajatao.bandcamp.com/album/the-rite-of-spring"]{◊trad{時代在召喚}（The Rite of Spring）}。喜欢的话多多支持！
