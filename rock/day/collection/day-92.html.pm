最近听到的好专辑有点太多了……发不过来。清脆的鼓点，浑厚的人声，饱满的管乐，灵动的键盘，满满的芝加哥气息。虽然他们是个英国摇滚乐队www。（封面也很芝加哥好吗！）这张专辑是这个乐队的专辑里我最喜欢的。

◊em{Fire And Brimstone} 一开头就是几个干净利落的鼓声，一下就让我打起精神。后面 groovy 的曲调节奏十分爽快。封面曲 ◊em{We Can’t Go On Meeting Like This} 很有范。B面的 ◊em{Heaven Knows (Where You’ve Been)} 很有感染力。有点 ◊em{Knocking on Heaven’s Door} 的感觉，尤其是 Eric Clapton 的那个版本。

另外，这张专辑是 Terminal Passage 传的。Terminal Passage 是除 Xerf Xpec 外另一个传了一堆绝赞专辑的神奇人物。他传的好像更多偏向爵士放克这方面。

