封面上橙色的是◊jpns{東郷清丸}，蓝色的想必是◊jpns{後藤}。他们还给专辑设计了首页：◊link["https://togokiyomaru.com/gotototogo"]{「◊jpns{後藤と東郷}」}。他们封面的字体也很有趣！

第一首 ◊em{Summer Dance} 有很强的氛围，有些像在幽暗的水中，听到远处传来的乐声。后面的 ◊em{Vegetable} 里有一句“Holy, holy, holy shit”，听到的时候把我乐坏了。可惜不知道完整歌词是在说啥，估计在说2020年各种乱七八糟的事，我听到了 CDC，COVID 19，FAKE NEWS 啥的www

我看到标题后面的“EP”不懂是什么，就去查了查，发现是“Extended Play”的意思。就是说当年用黑胶唱片的时候，EP 上曲目数量介于 Single 和 LP 之间。LP 当然就是 “Long Play”。
