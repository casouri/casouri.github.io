◊body{
万物皆有终结。 ◊em{Abbey Road} 是最后一张 Beatles 专辑。最后一首歌 The End 结束后，再也不会有更多的 Beatles 歌曲在世上出现。同样，大二数学课上看到学姐到现在大四过半，不到两年的单恋也结束了◊fnref["puchi"]。The fab four 从四个夹克皮裤的反叛分子在 Hamburg 夜店摇滚，到四个西装拖把头男孩横扫世界，到四个迷幻嬉皮士青年为流行乐带来创新，到天台演唱会上四个严肃的摇滚男人最后一次现场演出。他们经历了传奇的八年，整个世界被四个人改变，他们自己也有翻天覆地的变化。

我经过这两年成长了些吗？两年时间毕竟不长，我的变化没有太大。我懂得更多知识，会做更多的菜，身体变好了些，认识了更多单词，余日摇滚写了五十多篇，玩 Splatoon 变强了一些，头发长到披肩……好像唯独感情上没感觉更成熟。不过没关系，以后会变好的。

和学姐聊过之后，心情轻松了很多，虽然心中依然空荡，但是之后也会变好吧。Beatles 解散的时候心中会不会也有这样的感情呢？虽然没有什么亲密的关系了（本来也基本没有），但学姐依然还在我的生活中，想聊天时还是有人理我。所以还不算太坏？

“Golden Slumbers” 里 Paul 的嘶吼，“Carry That Weight” 里四个人的合唱，“The End” 里四个人的独奏，都让我汗毛倒竖，心中充满感动。最后的 “The End” 结束后，心中久久不能平息。

