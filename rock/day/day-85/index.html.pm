本想等收到了专辑再写，但是已经拖了太久了，先写了再说www　这张专辑是 Xerf Xpec 推荐的，我忘了在哪个视频的评论里看到有人问他最喜欢的 City Pop 专辑，没记错的话他说是 ◊link["https://archive.casouri.cat/rock/day/day-64/index.html"]{◊em{Tokyo Sniper}} 和这张 ◊em{Silent Flight}。◊em{Silent Flight}，◊em{Tokyo Sniper} 和 ◊link["https://archive.casouri.cat/rock/day/day-69/index.html"]{◊em{CITY DIVE}} 同属一种风格，可以说是“现代 City Pop”。把几张专辑和原本的 City Pop相比较，能明显听出“现代”来，但我也说不上具体哪里“现代”。可能因为现代的鼓点和音效？感觉唱法也有变化。不变的是 City Pop 梦幻、放松的氛围。我不想说“迷离”，因为比 City Pop 迷离的流派大有人在，但是这种恰当好处的闲适放松是 City Pop 特有的，也是它的魅力所在。

这几张专辑都受◊jpns{流線形}影响颇深：◊jpns{サノトモミ}是◊jpns{流線形}第一章专辑的主唱，◊em{CITY DIVE} 的◊jpns{一十三十一}是 ◊em{Tokyo Sniper} 的主唱——可能“影响颇深”是说浅了www

不用多说，这张专辑的封面我也很喜欢，十分契合音乐！

最后，我上面提的几张专辑都还有 CD 售卖，喜欢的话多多支持！
