最近在整理油管上播放列表里收藏的专辑，准备之后统一备份。整理的时候看见了这首专辑，想到这张专辑如此经典优秀，就贴上来（再混一天）。这好像还是我第一次写余日摇滚的时候输入中文音乐人的名字……《新长征路上的摇滚》词有文学深度，曲新颖成熟，并且最重要的是——好听，让人难以相信是中文摇滚作品，还是1989年的作品。最近听了一些五条人和二手玫瑰，之前听了些万能青旅啥的，这些乐队的作品，歌词有点意思，编曲演奏还是差点。唉，希望以后能有《新长征路上的摇滚》这样石破天惊的中文作品出现。

写这篇的时候我在把原来的博客源文件一篇篇迁移到新的框架上，顺便涂涂改改前面的评论，还挺有趣的。
