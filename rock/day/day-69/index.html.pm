无需多言，这张封面我也很喜欢www。◊ruby{◊jpns{一十三十一}◊rt{hitomitoi}}，本名◊jpns{天野一十三}，从◊link["https://www.hitomitoi.com/#about"]{她主页的介绍}来看，2002年出道，2012年这张专辑 ◊em{CITY DIVE} 成为话题作，是个慢热选手www。

听完 ◊em{Tokyo Sniper} 以后油管立刻给我推荐了这张，也是 Xerf Xpec 传的（谢谢你，XX老哥）。我搞了很久都没弄明白到底 ◊em{Tokyo Sniper} 的主唱和这张的是不是一个人🤔，听起来真的很像是同一个。这张专辑的风格跟 ◊em{Tokyo Sniper} 很像，曲风灵动，曲调优美，主唱声音独特，时不时有些有趣的独奏。总体来说 ◊em{CITY DIVE} 比 ◊em{Tokyo Sniper} 略高一线。

