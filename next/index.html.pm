Maybe less so nowadays, but the Internet used to be an archipelago of personal informational islands. You surf around the net, visit others’ little island, and they points you to other interesting islands to visit.

That’s what I’m going to do here—point you to other people’s islands. If you find my island interesting, you’ll probably find these islands interesting too:

◊link["https://practicaltypography.com"]{◊em{Practical Typography}} is a web book that efficiently introduces typography best-practices. I probably have mentioned it too many times. But if a single soul go ahead and reads it upon my recommendation, and produces better documents, it is well worth it.

